#include "Circle.h"



/////////////////////////////////////////////////////
//ctor
/////////////////////////////////////////////////////

Circle::Circle(double raduis )
	:m_raduis(raduis),Shape("Circle")
{
}
/////////////////////////////////////////////////////
//
/////////////////////////////////////////////////////

 const double Circle::getArea()
{
	return 3.145 *(m_raduis*m_raduis);
}
/////////////////////////////////////////////////////
//
/////////////////////////////////////////////////////

const double Circle::getPerimeter()
{
	return 2*3.145*(m_raduis);
}
/////////////////////////////////////////////////////
//
/////////////////////////////////////////////////////

