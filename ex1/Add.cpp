#include "Add.h"

using std::string;

Add::Add(std::shared_ptr<Shape> s1, std::shared_ptr<Shape> s2)
	:m_s1(s1),m_s2(s2)
	, Shape(string("Add (").append(s1->getName()).append(" + ").append(s2->getName()).append(")"))

{
}
/////////////////////////////////////////////////////
//
/////////////////////////////////////////////////////

const double Add::getArea() 
{
	return m_s1->getArea()+m_s2->getArea();
}
/////////////////////////////////////////////////////
//
/////////////////////////////////////////////////////

const double Add::getPerimeter() 
{
	return m_s1->getPerimeter()+m_s2->getPerimeter();
}
/////////////////////////////////////////////////////
//
/////////////////////////////////////////////////////

void Add::draw() const
{
	m_s1->printDimensions();
	m_s1->draw();
	std::cout << std::endl;
	m_s2->printDimensions();
	m_s2->draw();
}
/////////////////////////////////////////////////////
//
/////////////////////////////////////////////////////

void Add::printDimensions() const
{
	std::cout << "(";
	m_s1->printDimensions();
	std::cout << " + ";
	m_s2->printDimensions();
	std::cout << ")" ;
}
