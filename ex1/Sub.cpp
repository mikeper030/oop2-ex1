#include "Sub.h"


/////////////////////////////////////////////////////
//
/////////////////////////////////////////////////////

Sub::Sub(std::shared_ptr<Shape> s1, std::shared_ptr<Shape> s2)
	:m_s1(s1), m_s2(s2)
	, Shape(string("Sub (").append(s1->getName()).append(" - ").append(s2->getName()).append(")"))

{
}
/////////////////////////////////////////////////////
//
/////////////////////////////////////////////////////

const double Sub::getArea()
{
	return m_s1->getArea()-m_s2->getArea();
}
/////////////////////////////////////////////////////
//
/////////////////////////////////////////////////////

const double Sub::getPerimeter()
{
	return m_s1->getPerimeter()-m_s2->getPerimeter();
}
/////////////////////////////////////////////////////
//
/////////////////////////////////////////////////////

void Sub::draw() const
{
	m_s1->printDimensions();
	m_s1->draw();
	std::cout << std::endl;
	m_s2->printDimensions();
	m_s2->draw();
	
}
/////////////////////////////////////////////////////
//
/////////////////////////////////////////////////////

void Sub::printDimensions() const
{
	std::cout << "(";
	m_s1->printDimensions();
	std::cout << "-";
	m_s2->printDimensions();
	std::cout << ")" ;
}
